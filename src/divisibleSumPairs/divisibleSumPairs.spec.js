import { divisibleSumPairs } from "./divisibleSumPairs";

describe("divisibleSumPairs", () => {
  it("should return number of pairs that is divisible by k", () => {
    expect(divisibleSumPairs(6, 5, [1, 2, 3, 4, 5, 6])).toBe(3);
    expect(divisibleSumPairs(6, 3, [1, 3, 2, 6, 1, 2])).toBe(5);
    expect(
      divisibleSumPairs(
        100,
        22,
        [
          43, 95, 51, 55, 40, 86, 65, 81, 51, 20, 47, 50, 65, 53, 23, 78, 75,
          75, 47, 73, 25, 27, 14, 8, 26, 58, 95, 28, 3, 23, 48, 69, 26, 3, 73,
          52, 34, 7, 40, 33, 56, 98, 71, 29, 70, 71, 28, 12, 18, 49, 19, 25, 2,
          18, 15, 41, 51, 42, 46, 19, 98, 56, 54, 98, 72, 25, 16, 49, 34, 99,
          48, 93, 64, 44, 50, 91, 44, 17, 63, 27, 3, 65, 75, 19, 68, 30, 43, 37,
          72, 54, 82, 92, 37, 52, 72, 62, 3, 88, 82, 71,
        ]
      )
    ).toBe(216);
    expect(
      divisibleSumPairs(
        100,
        22,
        [
          71, 44, 2, 93, 66, 27, 41, 99, 49, 68, 60, 16, 45, 21, 71, 96, 89, 91,
          60, 21, 43, 9, 56, 48, 25, 96, 91, 99, 73, 22, 48, 32, 27, 71, 72, 90,
          9, 62, 68, 70, 77, 98, 2, 32, 69, 51, 99, 35, 47, 83, 82, 43, 87, 47,
          40, 54, 53, 85, 78, 31, 98, 26, 56, 100, 88, 43, 77, 81, 58, 31, 46,
          70, 57, 8, 16, 53, 8, 61, 22, 62, 75, 94, 91, 29, 95, 69, 22, 12, 88,
          5, 87, 90, 10, 86, 86, 85, 73, 95, 87, 53,
        ]
      )
    ).toBe(182);
  });
});
